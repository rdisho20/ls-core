def transpose(matrix):
    result = []
    new_column_length = len(matrix[0])

    for _ in range(new_column_length):
        result.append([])
    
    for row_elem in range(len(result)):
        for col_elem in range(len(matrix)):
            result[row_elem].append(matrix[col_elem][row_elem])
    
    return result


